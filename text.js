function verificarResposta(silaba) {
    if (silaba === 'la') {
        document.getElementById('resultado').innerText = 'Resposta correta! Parabéns!';
    } else {
        document.getElementById('resultado').innerText = 'Resposta errada. Tente novamente.';
    }
}



